package section5;

public class ForRecap66 {
    public static void main(String[] args) {
        for(int i=1; i<7; i+=2){
            System.out.println("number "+i);
        }
        for(int i=1; i<0; i+=2){
            System.out.println("number "+i);
        }
        for(int i=100; i<130; i+=10){
            System.out.println("number "+i);
        }
    }
}
